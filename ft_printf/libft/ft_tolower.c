#include "libft.h"

int	ft_tolower(int c)
{
	if (c == -1)
	{
		return (-1);
	}
	else if (c < -1)
	{
		return (256 + c);
	}
	else if (c >= 'A' && c <= 'Z')
	{
		return (c + 32);
	}
	return (c);
}
